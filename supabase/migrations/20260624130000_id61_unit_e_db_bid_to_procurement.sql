-- -----------------------------------------------------------------------------
-- 2. public.bid_response_auto_version() -> public.form_response_auto_version()
--    Create new fn (body verbatim), repoint trigger, mirror grants, drop old.
-- -----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION "public"."form_response_auto_version"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public', 'extensions'
    AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    NEW.version := 1;
  ELSIF TG_OP = 'UPDATE' THEN
    -- Only increment if response content actually changed
    IF NEW.response_text IS DISTINCT FROM OLD.response_text
       OR NEW.response_text_advanced IS DISTINCT FROM OLD.response_text_advanced
       OR NEW.metadata IS DISTINCT FROM OLD.metadata THEN
      NEW.version := OLD.version + 1;
    END IF;
  END IF;
  RETURN NEW;
END;
$$;

ALTER FUNCTION "public"."form_response_auto_version"() OWNER TO "postgres";

-- Mirror the original grants (squash baseline L12323-12324).
REVOKE ALL ON FUNCTION "public"."form_response_auto_version"() FROM PUBLIC;
REVOKE EXECUTE ON FUNCTION "public"."form_response_auto_version"() FROM anon;
GRANT ALL ON FUNCTION "public"."form_response_auto_version"() TO "service_role";

-- Repoint the trigger to the renamed function (CREATE OR REPLACE TRIGGER is
-- idempotent in PG15+; replaces the existing form_response_set_version trigger).
CREATE OR REPLACE TRIGGER "form_response_set_version" BEFORE INSERT OR UPDATE ON "public"."form_responses" FOR EACH ROW EXECUTE FUNCTION "public"."form_response_auto_version"();

-- Drop the old function now that nothing references it (trigger repointed above).
DROP FUNCTION IF EXISTS "public"."bid_response_auto_version"();

