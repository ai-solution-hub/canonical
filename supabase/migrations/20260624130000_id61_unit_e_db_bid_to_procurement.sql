CREATE OR REPLACE FUNCTION "public"."get_form_summary"("workspace_id" "uuid") RETURNS json
    LANGUAGE "sql" STABLE
    SET "search_path" TO 'public', 'extensions'
    AS $$
  SELECT json_build_object(
    'workspace_id', workspace_id,
    'total_questions', (SELECT COUNT(*) FROM form_questions WHERE form_questions.workspace_id = get_form_summary.workspace_id),
    'status_breakdown', (
      SELECT json_agg(json_build_object('status', status, 'count', cnt) ORDER BY cnt DESC)
      FROM (SELECT status, COUNT(*) AS cnt FROM form_questions WHERE form_questions.workspace_id = get_form_summary.workspace_id GROUP BY status) sub),
    'confidence_breakdown', (
      SELECT json_agg(json_build_object('posture', confidence_posture, 'count', cnt) ORDER BY cnt DESC)
      FROM (SELECT confidence_posture, COUNT(*) AS cnt FROM form_questions
        WHERE form_questions.workspace_id = get_form_summary.workspace_id AND confidence_posture IS NOT NULL GROUP BY confidence_posture) sub),
    'responses_count', (
      SELECT COUNT(*) FROM form_responses br JOIN form_questions bq ON bq.id = br.question_id WHERE bq.workspace_id = get_form_summary.workspace_id),
    'review_status_breakdown', (
      SELECT json_agg(json_build_object('status', review_status, 'count', cnt) ORDER BY cnt DESC)
      FROM (SELECT br.review_status, COUNT(*) AS cnt FROM form_responses br
        JOIN form_questions bq ON bq.id = br.question_id WHERE bq.workspace_id = get_form_summary.workspace_id GROUP BY br.review_status) sub),
    'sections', (
      SELECT json_agg(json_build_object('section', section_name, 'question_count', cnt, 'completed', completed_cnt) ORDER BY min_seq)
      FROM (SELECT bq.section_name, COUNT(*) AS cnt, COUNT(*) FILTER (WHERE bq.status = 'complete') AS completed_cnt,
        MIN(bq.section_sequence) AS min_seq FROM form_questions bq WHERE bq.workspace_id = get_form_summary.workspace_id GROUP BY bq.section_name) sub)
  );
$$;

