-- -----------------------------------------------------------------------------
-- 1. public.get_aggregate_win_rate_stats — unique_bids -> unique_procurements
--    (RETURNS TABLE column) + bid_outcome -> outcome (body-only CTE alias).
--
--    DROP+CREATE (NOT CREATE OR REPLACE): PostgreSQL forbids renaming a
--    RETURNS-TABLE output column via CREATE OR REPLACE (SQLSTATE 42P13 — "cannot
--    change return type of existing function"). The api wrapper depends on the
--    public function (SELECT * FROM public.get_aggregate_win_rate_stats()), so
--    drop the api wrapper FIRST, then the public function, then recreate both.
--    Verified the api wrapper is the SOLE dependent — no view / function /
--    trigger else references the public fn — so DROP without CASCADE is clean.
--    Both DROPs clear the function grants, so the original grants are RE-EMITTED
--    after each CREATE (public: squash L12454-56; api: id115 L1546-47).
-- -----------------------------------------------------------------------------
DROP FUNCTION IF EXISTS "api"."get_aggregate_win_rate_stats"();
DROP FUNCTION IF EXISTS "public"."get_aggregate_win_rate_stats"();

CREATE FUNCTION "public"."get_aggregate_win_rate_stats"() RETURNS TABLE("scope" "text", "total_citations" bigint, "winning_citations" bigint, "losing_citations" bigint, "pending_citations" bigint, "win_rate" numeric, "unique_items_cited" bigint, "unique_procurements" bigint)
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public', 'extensions'
    AS $$
BEGIN
  RETURN QUERY

  WITH citation_detail AS (
    SELECT
      ci.primary_domain,
      cc.cited_content_item_id,
      cc.citing_form_response_id,
      bq.workspace_id,
      w.domain_metadata->>'outcome' as outcome
    FROM public.citations cc
    JOIN content_items ci ON ci.id = cc.cited_content_item_id
    JOIN form_responses br ON br.id = cc.citing_form_response_id
    JOIN form_questions bq ON bq.id = br.question_id
    JOIN workspaces w ON w.id = bq.workspace_id
    WHERE cc.cited_kind = 'content_item'
  ),
  domain_stats AS (
    SELECT
      primary_domain as scope,
      COUNT(*)::bigint as total_citations,
      COUNT(*) FILTER (WHERE outcome = 'won')::bigint as winning_citations,
      COUNT(*) FILTER (WHERE outcome = 'lost')::bigint as losing_citations,
      COUNT(*) FILTER (WHERE outcome IS NULL
                        OR outcome NOT IN ('won', 'lost', 'withdrawn'))::bigint as pending_citations,
      CASE
        WHEN COUNT(*) FILTER (WHERE outcome IN ('won', 'lost')) > 0 THEN
          ROUND(
            COUNT(*) FILTER (WHERE outcome = 'won')::numeric /
            COUNT(*) FILTER (WHERE outcome IN ('won', 'lost'))::numeric,
            2
          )
        ELSE 0
      END as win_rate,
      COUNT(DISTINCT cited_content_item_id)::bigint as unique_items_cited,
      COUNT(DISTINCT workspace_id)::bigint as unique_procurements
    FROM citation_detail
    GROUP BY primary_domain
  ),
  overall AS (
    SELECT
      'overall'::text as scope,
      COUNT(*)::bigint as total_citations,
      COUNT(*) FILTER (WHERE outcome = 'won')::bigint as winning_citations,
      COUNT(*) FILTER (WHERE outcome = 'lost')::bigint as losing_citations,
      COUNT(*) FILTER (WHERE outcome IS NULL
                        OR outcome NOT IN ('won', 'lost', 'withdrawn'))::bigint as pending_citations,
      CASE
        WHEN COUNT(*) FILTER (WHERE outcome IN ('won', 'lost')) > 0 THEN
          ROUND(
            COUNT(*) FILTER (WHERE outcome = 'won')::numeric /
            COUNT(*) FILTER (WHERE outcome IN ('won', 'lost'))::numeric,
            2
          )
        ELSE 0
      END as win_rate,
      COUNT(DISTINCT cited_content_item_id)::bigint as unique_items_cited,
      COUNT(DISTINCT workspace_id)::bigint as unique_procurements
    FROM citation_detail
  )
  SELECT * FROM overall
  UNION ALL
  SELECT * FROM domain_stats
  ORDER BY scope;
END;
$$;

ALTER FUNCTION "public"."get_aggregate_win_rate_stats"() OWNER TO "postgres";

