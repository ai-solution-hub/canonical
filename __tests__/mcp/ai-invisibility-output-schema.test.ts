// ===========================================================================
// B-INV-39 — consumption surfaces present AI enrichments as native data
// ===========================================================================

describe('B-INV-39 — AI-invisibility on consumption surfaces', () => {
  it('`get` (content item detail) presents classification + summary as native fields', () => {
    const markdown = formatContentItem(makeEnrichedItem());

    // The enrichment-derived fields are present as plain, native data...
    expect(markdown).toContain('## Summary');
    expect(markdown).toContain(
      'A summary of the quality management system requirements.',
    );
    expect(markdown).toContain('**Domain:** compliance > quality-management');
    expect(markdown).toContain('**Keywords:** ISO 9001, quality, audit');
    // ...with NO AI-advertising framing around them.
    expectPresentsEnrichmentsAsNative('get', markdown);
  });

  it('`get` does not label the summary or keywords as an AI product', () => {
    const markdown = formatContentItem(makeEnrichedItem());
    // Native field labels, not "AI Summary" / "AI Keywords".
    expect(markdown).not.toMatch(/##\s*AI\s+Summary/i);
    expect(markdown).not.toMatch(/\*\*AI\s+Keywords:/i);
    expect(markdown).not.toMatch(/\bClassified by\b/i);
  });

  it('`find` search results present AI summary + AI classification as native', () => {
    const markdown = formatSearchResults('data retention', [
      makeSearchResult(),
    ]);

    expect(markdown).toContain(
      'A summary describing the retention periods for personal data.',
    );
    expect(markdown).toContain('**Domain:** compliance > data-protection');
    // Relevance is the vector-similarity score, surfaced as a plain metric.
    expect(markdown).toContain('**Relevance:** 87%');
    expectPresentsEnrichmentsAsNative('find', markdown);
  });

  it('`find` Q&A results present the AI-generated summary as native', () => {
    const markdown = formatQASearchResults('data retention', [
      makeSearchResult(),
    ]);
    expect(markdown).toContain(
      'A summary describing the retention periods for personal data.',
    );
    expectPresentsEnrichmentsAsNative('find (q&a)', markdown);
  });

  it('intelligence summary presents AI scoring + AI summary as native', () => {
    const markdown = formatIntelligenceSummary(makeIntelligenceData());

    // The AI relevance score renders as a native badge (HIGH/MED/LOW + %)...
    expect(markdown).toContain('[HIGH 88%]');
    // ...and the per-article AI summary renders as plain body text, with no
    // "AI summary:" label or model attribution.
    expect(markdown).toContain(
      'The framework introduces new thresholds for SME suppliers.',
    );
    expect(markdown).not.toMatch(/\bAI\s+summary:/i);
    expectPresentsEnrichmentsAsNative('intelligence summary', markdown);
  });

  it('renders cleanly when a content item has not yet been enriched (no fabricated AI framing)', () => {
    const markdown = formatContentItem(
      makeEnrichedItem({
        summary: null,
        ai_keywords: null,
        classification_confidence: null,
        primary_domain: null,
        primary_subtopic: null,
      }),
    );
    // No summary section is invented, and nothing advertises AI absence either.
    expect(markdown).not.toContain('## Summary');
    expectPresentsEnrichmentsAsNative('get (unenriched)', markdown);
  });
});

// ===========================================================================
// B-INV-37 — outputSchema is the forward standard for NEW entries only
// ===========================================================================

describe('B-INV-37 — outputSchema forward-standard (new consolidated entries only)', () => {
  let toolsWithOutputSchema: Set<string>;
  let registeredNames: Set<string>;

  beforeAll(async () => {
    const mockServer = createMockMcpServer();
    await registerTools(mockServer.server);
    registeredNames = new Set(mockServer.toolList.map((t) => t.name));
    toolsWithOutputSchema = new Set(
      mockServer.toolList
        .filter((t) => t.config.outputSchema !== undefined)
        .map((t) => t.name),
    );
  });

  // The four NEW outcome-shaped consolidated entries from {71.7-71.10} that
  // CAN carry an object outputSchema (i.e. excludes the two z.union entries).
  const NEW_OBJECT_SCHEMA_ENTRIES = [
    'get', // {71.10} — get one-or-many
    'assign', // {71.10} — assign one-or-many
    'where_are_we_exposed', // {71.8}  — five-layer exposure
    'whats_in_my_queue', // {71.9}  — faceted queue
  ] as const;

  // `find`'s response is a z.union — outputSchema is intentionally DEFERRED
  // (the MCP SDK's normalizeObjectSchema() returns undefined for unions,
  // crashing validateToolOutput on undefined._zod). `find_duplicates` is no
  // longer a union itself (ID-131.15, G-DEDUP legacy dedup-family
  // retirement, S446, removed the sibling `scope: 'all'` branch that made
  // it one) but still omits outputSchema — kept minimal-footprint rather
  // than retrofitted, consistent with the other entries in this group. This
  // is the documented "where it fits" exclusion, not a B-INV-37 miss.
  const NEW_UNION_SCHEMA_ENTRIES = ['find', 'find_duplicates'] as const;

  // Retiring/changing ("refine"-verdict) tools that survived the {71}
  // consolidation: they MUST NOT be retrofitted with outputSchema (B-INV-37
  // *Fail* clause). Sourced from RESEARCH.md §4 keep-concept/refine verdicts.
  const RETIRING_OR_CHANGING_TOOLS = [
    'classify_content', // refine — apply §5 grounding standard
    'generate_summary', // refine — output_config.format (§5)
    'get_intelligence_summary', // refine — per-persona so-what consumption read
    'get_entity_relationships', // refine — promote to ontology-grounding affordance
    'get_reorientation', // refine — widen beyond KH state
    'suggest_content_creation', // refine — resolution affordance of exposure
    'create_content_item', // refine — track external-folder-canonical reality
    'get_form_question', // refine + rename — form-question
    'cite_content', // refine — align description post-rename
    'list_active_procurement', // refine — under completing-forms
    'get_procurement_detail', // refine — under completing-forms
  ] as const;

  it('every new object-schema consolidated entry declares an outputSchema', () => {
    for (const name of NEW_OBJECT_SCHEMA_ENTRIES) {
      expect(
        registeredNames.has(name),
        `consolidated entry ${name} must be registered`,
      ).toBe(true);
      expect(
        toolsWithOutputSchema.has(name),
        `new entry ${name} must declare an outputSchema (B-INV-37 forward standard)`,
      ).toBe(true);
    }
  });

  it('find and find_duplicates intentionally omit outputSchema', () => {
    for (const name of NEW_UNION_SCHEMA_ENTRIES) {
      expect(
        registeredNames.has(name),
        `consolidated entry ${name} must be registered`,
      ).toBe(true);
      expect(
        toolsWithOutputSchema.has(name),
        `${name} must NOT declare an outputSchema — see NEW_UNION_SCHEMA_ENTRIES comment above`,
      ).toBe(false);
    }
  });

  it('no retiring/changing tool is retrofitted with an outputSchema', () => {
    for (const name of RETIRING_OR_CHANGING_TOOLS) {
      if (!registeredNames.has(name)) continue; // tolerant if a tool later retires fully
      expect(
        toolsWithOutputSchema.has(name),
        `retiring/changing tool ${name} must NOT be retrofitted with an ` +
          `outputSchema (B-INV-37 *Fail* clause)`,
      ).toBe(false);
    }
  });
});
