// ---------------------------------------------------------------------------
// ID-71.21 — AI-invisibility (B-INV-39) + outputSchema forward-standard
// (B-INV-37) invariant-level coverage.
//
// The per-entry suites (find-tool, content-consolidation, dedup-consolidation,
// where-are-we-exposed-tool, whats-in-my-queue-tool) already assert that each
// NEW consolidated entry from {71.7}/{71.8}/{71.9}/{71.10} carries (or, for the
// two z.union entries, intentionally defers) an `outputSchema`. This suite owns
// the two CROSS-CUTTING invariants that no per-entry suite owns:
//
//   B-INV-39 (PRODUCT.md §E.39, ⇐ SYNTHESIS §1.3 ai-visibility-policy):
//     AI-driven enrichments (classification, summarisation, intelligence
//     scoring) present on CONSUMPTION surfaces as NATIVE data — the platform
//     behaves as if the data was always there. *Fail:* a consumption surface
//     advertises an AI enrichment as a feature ("AI generated this", model
//     attribution, enrichment-lineage framing). The admin Provenance surface is
//     the only carve-out, and it is not a consumption read formatter.
//
//   B-INV-37 (PRODUCT.md §E.37, ⇐ RESEARCH §5.2 / bl-26):
//     `outputSchema` is the forward standard for NEW entries ONLY. *Fail:* a
//     retiring/changing tool is retrofitted, or a new entry omits it where it
//     clearly fits.
//
// Behaviour-first per test-philosophy.md: B-INV-39 asserts on the OBSERVABLE
// rendered markdown a consumption surface returns (the bytes an MCP client
// shows the user); B-INV-37 asserts on the OBSERVABLE registered tool config
// (the `outputSchema` slot the MCP SDK reads at registration), exercised
// through the public `registerTools` registration pathway — not handler
// internals.
// ---------------------------------------------------------------------------

// AI-advertising / enrichment-lineage phrases that MUST NOT appear on any
// consumption surface. Matched case-insensitively against rendered markdown.
const AI_ADVERTISING_PATTERNS: RegExp[] = [
  /\bAI[- ]generated\b/i,
  /\bAI[- ]powered\b/i,
  /\bAI[- ]driven\b/i,
  /\bAI[- ]assisted\b/i,
  /\bAI[- ]suggested\b/i,
  /\bgenerated by (?:AI|Claude|the model|an? (?:language )?model)\b/i,
  /\benriched by AI\b/i,
  /\bAI enrichment\b/i,
  /\busing claude\b/i,
  /\bvia (?:AI|Claude)\b/i,
  /\bmachine[- ]generated\b/i,
  // Model-attribution footer ("…using claude-…", "model: claude-…").
  /\busing\s+claude-/i,
  /\bmodel:\s*claude/i,
];

function expectPresentsEnrichmentsAsNative(label: string, markdown: string) {
  for (const pattern of AI_ADVERTISING_PATTERNS) {
    expect(
      pattern.test(markdown),
      `${label} consumption surface must present enrichments as native, ` +
        `not advertise AI (matched ${pattern}). B-INV-39.`,
    ).toBe(false);
  }
}

