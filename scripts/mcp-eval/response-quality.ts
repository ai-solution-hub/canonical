// ---------------------------------------------------------------------------
// JSON-RPC client — same pattern as protocol-compliance.ts
// ---------------------------------------------------------------------------

let requestId = 1;

interface JsonRpcResponse {
  jsonrpc: '2.0';
  id: number;
  result?: unknown;
  error?: { code: number; message: string; data?: unknown };
}

interface ContentBlock {
  type: string;
  text?: string;
  resource?: unknown;
}

interface ToolCallResult {
  content: ContentBlock[];
  isError?: boolean;
}

const REQUEST_TIMEOUT_MS = 30_000; // 30 seconds per tool call

async function mcpRequest(
  method: string,
  params: Record<string, unknown>,
  accessToken: string,
): Promise<JsonRpcResponse> {
  const id = requestId++;
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), REQUEST_TIMEOUT_MS);

  try {
    const response = await fetch(MCP_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Accept: 'application/json, text/event-stream',
        Authorization: `Bearer ${accessToken}`,
      },
      body: JSON.stringify({
        jsonrpc: '2.0',
        id,
        method,
        params,
      }),
      signal: controller.signal,
    });

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}: ${await response.text()}`);
    }

    const contentType = response.headers.get('content-type') ?? '';

    if (contentType.includes('application/json')) {
      return (await response.json()) as JsonRpcResponse;
    }

    if (contentType.includes('text/event-stream')) {
      // Read SSE stream incrementally to avoid hanging on open connections
      const reader = response.body?.getReader();
      if (!reader) {
        throw new Error('No readable stream in SSE response');
      }

      const decoder = new TextDecoder();
      let buffer = '';

      while (true) {
        const { done, value } = await reader.read();
        if (value) {
          buffer += decoder.decode(value, { stream: !done });
        }

        // Try to extract JSON-RPC response from accumulated SSE events
        const events = buffer.split('\n\n').filter(Boolean);
        for (const event of events) {
          for (const line of event.split('\n')) {
            if (line.startsWith('data: ')) {
              try {
                const parsed = JSON.parse(line.slice(6)) as JsonRpcResponse;
                // Found a valid response — cancel the reader and return
                reader.cancel().catch(() => {});
                return parsed;
              } catch {
                // Not valid JSON yet, continue
              }
            }
          }
        }

        if (done) break;
      }

      throw new Error('No JSON-RPC response found in SSE stream');
    }

    throw new Error(`Unexpected content type: ${contentType}`);
  } finally {
    clearTimeout(timeout);
  }
}

/**
 * Call an MCP tool and return the text content.
 * Returns an object with errorMessage if the call fails.
 */
async function callTool(
  toolName: string,
  args: Record<string, unknown>,
  accessToken: string,
): Promise<{
  text: string;
  charCount: number;
  isError: boolean;
  errorMessage?: string;
}> {
  try {
    const response = await mcpRequest(
      'tools/call',
      { name: toolName, arguments: args },
      accessToken,
    );

    if (response.error) {
      return {
        text: '',
        charCount: 0,
        isError: true,
        errorMessage: `RPC error: ${response.error.message}`,
      };
    }

    const result = response.result as ToolCallResult;
    if (!result.content || !Array.isArray(result.content)) {
      return {
        text: '',
        charCount: 0,
        isError: true,
        errorMessage: 'Response missing content array',
      };
    }

    const textContent = result.content
      .filter((b) => b.type === 'text')
      .map((b) => b.text ?? '')
      .join('');

    return {
      text: textContent,
      charCount: textContent.length,
      isError: result.isError === true,
    };
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    return {
      text: '',
      charCount: 0,
      isError: true,
      errorMessage: msg.includes('abort')
        ? `Timeout after ${REQUEST_TIMEOUT_MS / 1000}s`
        : msg,
    };
  }
}

